package jcog.reflect;

import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.assertTrue;

/**
 *
 * @author Kamnev Georgiy (nt.gocha@gmail.com)
 */
public class ByteArrTest {
    

    @Test
    public void test(){
            byte[] data1 = { 1 };
            
            ByteArrayConvertor conv = new ByteArrayConvertor();
            String text1 = conv.toString.apply(data1);
            System.out.println(""+text1);
            
            Object dobj1 = conv.toValue.apply(text1);
            assertTrue( dobj1!=null );
            assertTrue( data1.getClass().equals(dobj1.getClass()) );
            
            byte[] bdata1 = (byte[])dobj1;
            
            assertTrue(data1.length == bdata1.length);
            for( int i=0; i<data1.length; i++ ){
                assertTrue( data1[i] == bdata1[i] );
            }

    }
}
